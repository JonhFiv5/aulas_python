# Os operadores são:
# Soma
print('10 + 5 = ', 10 + 5)
# Subtração
print('10 - 5 = ', 10 - 5)
# Multiplicação
print('10 * 2 = ', 10 * 2)
# Divisão
print('10 / 5 = ', 10 / 5)
# Exponenciação
print('4 ** 2 = ', 4 ** 2)
# Módulo (resto da divisão)
print('10 % 3 = ', 10 % 3)
# Divisão inteira
print('10 // 3 = ', 10 // 3)

# Podemos multiplicar uma string
print('<>' * 20)
# O sinal de adição serve para concatenar strings
print('João' + ' ' + 'Kaíque')
# Para concatenar um valor numérico nós usamos casting
print('Eu tenho' + ' ' + str(20) + ' ' + 'anos.')

