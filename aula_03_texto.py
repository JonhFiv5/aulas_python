# strings(texto) e aspas em Python
# str - string

# Aspas simples imprimem duplas
print('"Ser ou não ser...".')
# Aspas duplas imprimem simples
print("Caixa d'água.")

# Ou podemos usar o caractere de escape (barra invertida)
print('Conor O\'Malley.')

# A barra invertida também pode ser usada para quebra de linha
print('Linha 1 \nLinha 2')

# Para imprimir \n é só colocar um r antes da string
# Assim o comando não vai ser executado
print(r'Estou imprimindo \n')
